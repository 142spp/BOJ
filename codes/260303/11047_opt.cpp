/*
문제 요약
- 동전 종류 N개가 있고, 각 동전은 무한히 많이 있다.
- 동전 가치의 합을 K로 만들 때 필요한 동전 개수의 최솟값을 구한다.

입력 조건
- 1 <= N <= 10, 1 <= K <= 100,000,000
- 동전 가치 A[i]는 오름차순
- A[1] = 1
- i >= 2 에서 A[i]는 A[i-1]의 배수

출력
- K원을 만들기 위한 최소 동전 개수

사고 과정 (왜 그리디가 맞는가)
1) 동전이 오름차순이고, 큰 동전이 항상 작은 동전의 배수 구조다.
2) 따라서 큰 동전 1개를 작은 동전 여러 개로 바꾸면 동전 개수는 줄어들지 않는다.
   (같아지거나 오히려 늘어난다)
3) 그래서 매 단계에서 "현재 만들 수 있는 가장 큰 동전"을 최대한 많이 쓰는 선택이
   전체 최소 개수를 해치지 않는다.
4) 위 선택을 큰 동전부터 반복하면 최적해를 얻는다.
*/
#include <bits/stdc++.h>

using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, k;
    cin >> n >> k;

    // 입력은 오름차순으로 주어짐(작은 동전 -> 큰 동전)
    vector<int> coins(n);
    for (int i = 0; i < n; ++i) {
        cin >> coins[i];
    }

    int count = 0;
    // 가장 큰 동전부터 가능한 만큼 사용(그리디)
    for (int i = n - 1; i >= 0; --i) {
        // 현재 금액보다 큰 동전은 사용할 수 없음
        if (coins[i] > k) {
            continue;
        }
        // 이 동전을 몇 개 쓸 수 있는지 더하고, 남은 금액 갱신
        count += k / coins[i];
        k %= coins[i];
        // 남은 금액이 0이면 더 볼 필요 없음
        if (k == 0) {
            break;
        }
    }

    cout << count << '\n';
    return 0;
}
